class  Loop
{
	public static void main(String[] args) 
	{
		
		
		for(int i=1;i<=10;i++){
		System.out.print(i);
		}
		System.out.println();
		
		for(char ch='0';ch<='9'; ch++){
		System.out.print(ch);
		}
		System.out.println();
		
		for(char c='A';c<='z';c++){
		System.out.println(c);
		}
		
		System.out.println();
		for(char c='z';c<='a';c--){
		System.out.println(c);
		
		}
		System.out.println();
		for(int c=1;c<=127;c++){
		System.out.println(c+ " " + (char)c+ " ");
		}
		
		
	}
		
		
		
}
